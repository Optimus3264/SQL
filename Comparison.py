a=10
b=5

print('a=',a)
print('b=',b)

#a is equal to b (==)
print('a==b:', a==b) #False

#a is less than b (<)
print('a<b:',a<b) #False

#a is greater than b (>)
print('a>b:', a>b) #True

#a is not equal to b (!=)
print('a!=b:',a!=b) #True

#a is less than or equal to b (<=)
print('a<=b', a<=b)

#a is greater than or equal to b (>=)
print('a>=b', a>=b)